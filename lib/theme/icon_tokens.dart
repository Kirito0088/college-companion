/// Design Tokens: Icon Tokens
///
/// Standard icon sizes for College Companion per 01-design-tokens.md.
/// Material Symbols Rounded is the only icon set (per DESIGN.md).
/// Never hardcode icon sizes; reference these tokens instead.
library;

/// Icon size tokens following the defined icon scale.
abstract final class IconSizeTokens {
  /// Small icon size (20dp).
  static const double sm = 20;

  /// Default icon size (24dp).
  static const double md = 24;

  /// Large icon size (28dp).
  static const double lg = 28;

  /// Extra large icon size (32dp).
  static const double xl = 32;
}
